************************************ limit & offset ***********************************************

***********************syntax of limit********************************** 

select * from table_name where <condition> limit 2;

which means show only two  data from a perticular table which are satisfy the certain condition.


 
**************************syntax of offset and limit***************************

 
select * from table_name where <condition> limit 2,4;

which means it show  the data from 3rd position and showing more four data from a perticular table which are satisfy the certain condition.

